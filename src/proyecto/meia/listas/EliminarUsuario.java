    private void cbrPropietarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbrPropietarioItemStateChanged
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            cbrLista.removeAllItems();
            ManejoArchivosSecuencial manejoLista = new ManejoArchivosSecuencial(new File(Utilidades.ArchivoListasDeDistribucion), new File(Utilidades.ArchivoDescriptorListasDeDistribucion),
                    new File(Utilidades.ArchivoListasDeDistribucionSecuencial), new File(Utilidades.ArchivoDescriptorListasDeDistribucionSecuencial));
            List<String> datos;
            datos = manejoLista.obtenerListaUsuario(cbrPropietario.getSelectedItem().toString());
            int i = 0;
            while (i < datos.size())
            {
                cbrLista.addItem(datos.get(i).split("\\|")[0]);
                i++;
            }
        } catch (IOException ex) {
            
        }
    }//GEN-LAST:event_cbrPropietarioItemStateChanged

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try
        {
            ManejoArchivosIndizado indizado = new ManejoArchivosIndizado(new File(Utilidades.ArchivoIndice), new File(Utilidades.ArchivoDescriptorIndice), 
                    new File(Utilidades.ArchivoMaestro), new File(Utilidades.ArchivoDescriptorMaestro));
            ListaUsuario listaUsuario = new ListaUsuario(cbrPropietario.getSelectedItem().toString(), cbrLista.getSelectedItem().toString(), cbrUsuario.getSelectedItem().toString(), false);
            indizado.eliminar(listaUsuario);
            indizado.actualizarDescriptor(0, 1, "ID|Propietario|Lista|Usuario|Estatus|Siguiente|Linea");
        }
        catch (IOException ex)
        {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        } 
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

