    private void cbrListaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbrListaItemStateChanged
        // TODO add your handling code here:
        try
        {
            cbrUsuario.removeAllItems();
            ManejoArchivosIndizado indizado = new ManejoArchivosIndizado(new File(Utilidades.ArchivoIndice), new File(Utilidades.ArchivoDescriptorIndice), 
                    new File(Utilidades.ArchivoMaestro), new File(Utilidades.ArchivoDescriptorMaestro));
            List<String> datos = indizado.obtener(cbrPropietario.getSelectedItem().toString(), cbrLista.getSelectedItem().toString());
            int i = 0;
            while (i < datos.size())
            {
                cbrUsuario.addItem(datos.get(i).split("\\|")[2]);
                i++;
            }
        }
        catch (Exception ex) {
            
        }
    }//GEN-LAST:event_cbrListaItemStateChanged

